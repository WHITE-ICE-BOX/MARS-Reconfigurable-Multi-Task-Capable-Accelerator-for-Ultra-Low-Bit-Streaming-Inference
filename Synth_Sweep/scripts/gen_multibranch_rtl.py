def gen_adder_pe(n, M, alphas=None):
    """tp PE-wide adder, per-PE storage structure (deployed 'LUT fix' form).

    Every cfg-writable memory has exactly 1 write + 1 read port so it maps to
    distributed RAM: per-lane thresh/sign column arrays (lane p holds channels
    ch = s*PE+p) + per-lane copies of each branch's 256-entry contrib LUT
    (cfg write broadcast to all lane copies of the selected branch).
    """
    cfg = SITE[n]
    OUT_CH = cfg["OUT_CH"]
    PE = PE_TP[n]
    OUT_STEPS = OUT_CH // PE
    OW = OUTW_TP[n]
    SW = clog2(OUT_STEPS)
    PB = clog2(PE)
    OFFSET = cfg["UPW"] - cfg["HIDDEN"] // 2
    B = range(M)
    l = []
    a = l.append
    a("`timescale 1ns / 1ps")
    a("")
    a(f"// Stream_Adder_Threshold_mvau{n} — multi-branch M={M}, PE={PE} (throughput),")
    a("// per-PE storage structure (1W1R per array -> distributed RAM).")
    a("// Per lane p (ch = step*PE+p): total_q8 = (mvau_pop << 8) + (sign ? -sum : +sum),")
    a("// sum = sum over branches of lane-local contrib LUT lookups; out = (>= thr).")
    a("(* use_dsp = \"no\" *)")
    a(f"module Stream_Adder_Threshold_mvau{n} (")
    a("    input  wire                  aclk,")
    a("    input  wire                  aresetn,")
    a("")
    a(f"    input  wire [{PE*32-1}:0]         s_axis_1_tdata,")
    a("    input  wire                  s_axis_1_tvalid,")
    a("    output wire                  s_axis_1_tready,")
    a("")
    for b in B:
        a(f"    input  wire [{PE*8-1}:0]          s_axis_2_b{b}_tdata,")
        a(f"    input  wire                  s_axis_2_b{b}_tvalid,")
        a(f"    output wire                  s_axis_2_b{b}_tready,")
        a("")
    a(f"    output reg  [{OW-1}:0]           m_axis_tdata,")
    a("    output reg                   m_axis_tvalid,")
    a("    input  wire                  m_axis_tready,")
    a("")
    a("    input  wire [10:0]           cfg_waddr,")
    a("    input  wire [31:0]           cfg_wdata,")
    a("    input  wire                  cfg_wen,")
    a("    input  wire [1:0]            branch_sel,")
    a("    input  wire                  adapter_enable")
    a(");")
    a(f"    parameter PE        = {PE};")
    a(f"    parameter OUT_CH    = {OUT_CH};")
    a(f"    parameter OUT_STEPS = {OUT_STEPS};")
    a("")
    a("    localparam CFG_THRESH_BASE = 11'd1152;")
    a("    localparam CFG_SIGN_BASE   = 11'd1408;")
    a(f"    localparam signed [31:0] CONTRIB_OFFSET = {OFFSET};")
    for b in B:
        a(f"    localparam signed [31:0] ALPHA_Q8_B{b} = {alphas[b]};  // baked from checkpoint")
    a("")
    a(f"    reg [{SW-1}:0] step_cnt;")
    a("")
    a("    wire stall = m_axis_tvalid && !m_axis_tready;")
    all_v = " && ".join(f"s_axis_2_b{b}_tvalid" for b in B)
    a(f"    wire all_br_valid = {all_v};")
    a("    wire in_fire = s_axis_1_tvalid && all_br_valid && !stall;")
    a("    assign s_axis_1_tready = !stall && all_br_valid;")
    for b in B:
        others = " && ".join(["s_axis_1_tvalid"] + [f"s_axis_2_b{x}_tvalid" for x in B if x != b])
        a(f"    assign s_axis_2_b{b}_tready = !stall && {others};")
    a("")
    a("    reg s1_valid, s2_valid;")
    a(f"    wire [{PE-1}:0] cmp_bit;")
    a("")
    a("    // ---- per-lane storage + datapath ----")
    a("    genvar gp;")
    a("    generate")
    a("        for (gp = 0; gp < PE; gp = gp + 1) begin : g_lane")
    a("            // lane-column thresh/sign: entry s = channel s*PE+gp (1W1R)")
    a("            (* ram_style = \"distributed\" *) reg signed [31:0] th_mem [0:OUT_STEPS-1];")
    a("            (* ram_style = \"distributed\" *) reg               sg_mem [0:OUT_STEPS-1];")
    a("")
    a("            always @(posedge aclk) begin")
    a("                if (cfg_wen) begin")
    a("                    if (cfg_waddr >= CFG_THRESH_BASE && cfg_waddr < CFG_THRESH_BASE + OUT_CH")
    a(f"                        && ((cfg_waddr - CFG_THRESH_BASE) & (PE-1)) == gp)")
    a(f"                        th_mem[(cfg_waddr - CFG_THRESH_BASE) >> {PB}] <= $signed(cfg_wdata);")
    a("                    if (cfg_waddr >= CFG_SIGN_BASE && cfg_waddr < CFG_SIGN_BASE + OUT_CH")
    a(f"                        && ((cfg_waddr - CFG_SIGN_BASE) & (PE-1)) == gp)")
    a(f"                        sg_mem[(cfg_waddr - CFG_SIGN_BASE) >> {PB}] <= cfg_wdata[0];")
    a("                end")
    a("            end")
    a("")
    a("            reg signed [31:0] s1_mvau_pop;")
    for b in B:
        a(f"            reg signed [31:0] s1_contrib_b{b};")
    a("            reg signed [31:0] s1_threshold;")
    a("            reg               s1_adp_sign;")
    a("            reg signed [31:0] s2_total_q8;")
    a("            reg signed [31:0] s2_threshold;")
    csum = " + ".join(f"s1_contrib_b{b}" for b in B)
    a(f"            wire signed [31:0] contrib_sum = {csum};")
    a("")
    a("            always @(posedge aclk) begin")
    a("                if (!aresetn) begin")
    a("                    s1_mvau_pop <= 32'sd0; s1_threshold <= 32'sd0; s1_adp_sign <= 1'b0;")
    for b in B:
        a(f"                    s1_contrib_b{b} <= 32'sd0;")
    a("                    s2_total_q8 <= 32'sd0; s2_threshold <= 32'sd0;")
    a("                end else if (!stall) begin")
    a("                    if (s1_valid) begin")

    assign adp_sign_wire[23] = adp_sign_rom_23[step_cnt[0]][0];
    assign adp_sign_wire[24] = adp_sign_rom_24[step_cnt[0]][0];
    assign adp_sign_wire[25] = adp_sign_rom_25[step_cnt[0]][0];
    assign adp_sign_wire[26] = adp_sign_rom_26[step_cnt[0]][0];
    assign adp_sign_wire[27] = adp_sign_rom_27[step_cnt[0]][0];
    assign adp_sign_wire[28] = adp_sign_rom_28[step_cnt[0]][0];
    assign adp_sign_wire[29] = adp_sign_rom_29[step_cnt[0]][0];
    assign adp_sign_wire[30] = adp_sign_rom_30[step_cnt[0]][0];
    assign adp_sign_wire[31] = adp_sign_rom_31[step_cnt[0]][0];

    integer p;

    // =========================================================
    // Main pipeline
    // =========================================================
    always @(posedge aclk) begin
        if (!aresetn) begin
            m_axis_tvalid <= 1'b0;
            m_axis_tdata  <= 32'd0;
            s1_valid      <= 1'b0;
            s2_valid      <= 1'b0;
            step_cnt      <= 3'd0;

            for (p = 0; p < PE; p = p + 1) begin
                s1_mvau_pop[p]     <= 32'sd0;
                s1_adp_contrib[p]  <= 32'sd0;
                s1_threshold[p]    <= 32'sd0;
                s1_adp_sign[p]     <= 1'b0;
                s2_total_pop_q8[p] <= 32'sd0;
                s2_threshold[p]    <= 32'sd0;
            end
        end
        else if (!stall) begin
            // Stage 3: compare/output (unified Q8 >=)
            if (s2_valid) begin
                m_axis_tvalid <= 1'b1;
                for (p = 0; p < PE; p = p + 1) begin
                    m_axis_tdata[p] <= (s2_total_pop_q8[p] >= s2_threshold[p]) ? 1'b1 : 1'b0;
                end
            end
            else begin
                m_axis_tvalid <= 1'b0;
            end

            // Stage 2: widen MVAU popcount to Q8 and add/sub adapter contribution
            if (s1_valid) begin
                s2_valid <= 1'b1;
                for (p = 0; p < PE; p = p + 1) begin
                    s2_total_pop_q8[p] <= (s1_mvau_pop[p] <<< 8)
                                          + (s1_adp_sign[p] ? -s1_adp_contrib[p]
                                                            :  s1_adp_contrib[p]);
                    s2_threshold[p]    <= s1_threshold[p];
                end
            end
            else begin
                s2_valid <= 1'b0;
            end

            // Stage 1: capture
            if (in_fire) begin
                s1_valid <= 1'b1;
                for (p = 0; p < PE; p = p + 1) begin
                    s1_mvau_pop[p]    <= $signed(s_axis_1_tdata[(p*32) +: 32]);
                    s1_adp_contrib[p] <= adp_contrib_lut[s_axis_2_tdata[(p*8) +: 8]];
                    s1_threshold[p]   <= th_wire[p];
                    s1_adp_sign[p]    <= adp_sign_wire[p];
                end

                if (step_cnt == 3'd1)
                    step_cnt <= 3'd0;
                else
                    step_cnt <= step_cnt + 3'd1;
            end
            else begin
                s1_valid <= 1'b0;
            end
        end
    end

endmodule

// 2026-04-11 design notes (do NOT patch .dat files to chase off-by-ones
// — fix this RTL / regenerate the helper .dat from sw/ instead):
//
//   * memblock.dat, mvau1_in.dat, golden adapter weight .dat files are
//     FINN / helper truth. Do not patch them.
//   * Per-PE Q8 threshold + adapter-sign + alpha ROMs are regenerated by
//     sw/make_mvau1_q8_assets.py from the SAME RC_m1_full.tar used by
//     sw/pytorch_forward_mvau1.py, so they encode PyTorch ground truth.
//
// FINN BN absorption caveat:
//   When FINN runs batchnorm_to_multithreshold on bn1, channels whose
//   BN gamma is negative have their sign folded into the PRECEDING conv
//   weights that ship inside memblock.dat. For MVAU1 that's 9 channels
//   ([1,14,21,24,31,37,44,45,52]) whose FINN MVAU popcount P satisfies
//     2*P - K  =  -x_pyt     (inverted vs PyTorch bipolar dot)
//   The adapter-side up/down/rc weights are NOT BN-folded — they match
//   PyTorch sign convention for all 64 channels. So the RTL has to add
//   the adapter contribution for gamma>0 channels and SUBTRACT it for
//   gamma<0 channels in order to stay PyTorch-equivalent.
//
// Datapath (Q8 popcount domain, exact integer):
//
//   contrib    = (adp_pop - 24) * ALPHA_Q8          // no rounding
//   signed_c   = adp_sign ? -contrib : contrib      // per-channel flip
//   RTL_lhs_q8 = (s1_mvau_pop <<< 8) + signed_c
//   result_bit = RTL_lhs_q8 >= thresh_q8[ch]        // unified >=
//
//   Threshold encoding (signed Q8, stored per (PE, step)):
//     gamma > 0 (adp_sign = 0):  T = ceil(128 * (K + t_bip_float))
//     gamma < 0 (adp_sign = 1):  T = ceil(128 * (K - t_bip_float))
//     K = 64 * 9 = 576.
//
//   LUT rationale (unchanged — offset 24 compensates for the HIDDEN=16
//   zero-extension in Adapter_MVAU1's 32-bit XNOR):
//     adp_pop      = 16 + real_match
//     bipolar_dot  = 2*real_match - 16 = 2*(adp_pop - 24)
//     popcount-domain contribution = (alpha/2)*bipolar_dot
//                                  = (adp_pop - 24)*alpha
//     Stored as Q8: (adp_pop - 24) * ALPHA_Q8  (exact).
//
//   PE/step → channel mapping (unchanged from prior rev):
//     thresh_rom_p[s] / adp_sign_rom_p[s] correspond to ch = s*32 + p
//     (s = step_cnt, p = PE idx)
// =====================================================================

